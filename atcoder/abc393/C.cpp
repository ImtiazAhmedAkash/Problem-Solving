const int mod = 998244353;
const int N = 2e5 + 5;

void solve() {
    int n, m;
    cin >> n >> m;
    set<pair<int, int>> st;
    int ans = 0;
    while (m--) {
        int u, v;
        cin >> u >> v;
        if (u > v) swap(u, v);
        if (u == v || st.count({u, v})) ans++;
        st.insert({u, v});
    }
    cout << ans << nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    int T = 1;
    for (int t = 1; t <= T; t++) {
        solve();
    }
    
    return 0;
}