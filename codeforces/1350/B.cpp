using ll = long long;
constexpr char nl = '\n';

const int inf = 1e9;
const ll INF = 1e18;
const int mod = 998244353;
const int N = 2e5 + 5;

void solve() {
    int n;
    cin >> n;
    vector<int> A(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> A[i];
    }
    
    vector<int> dp(n + 1, 1);
    for (int i = 1; i <= n; i++) {
        for (int j = i + i; j <= n; j += i) {
            if (A[i] < A[j]) {
                dp[j] = max(dp[j], 1 + dp[i]);
            }
        }
    }
    
    cout << *max_element(dp.begin(), dp.end()) << nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    int T = 1;
    cin >> T;
    for (int t = 1; t <= T; t++) {
        solve();
    }
    
    return 0;
}